/**
 * Phase 1 — governance program unit tests.
 *
 * Coverage map (subtask numbers from spec/phases/phase-01-governance-program.md §4):
 *   4.1  initialize: owner + defaults + route_count
 *   4.2  set_defaults: owner-only
 *   4.3  whitelist_route: owner ✓, active admin ✓, non-admin ✗
 *   4.4  whitelist_route + get_route_terms: defaults when no overrides
 *   4.5  whitelist_route + get_route_terms: overrides when fully specified
 *   4.6  whitelist_route + get_route_terms: partial overrides resolve correctly
 *   4.7  whitelist_route: length-cap reverts
 *   4.8  disable_route: flips approved; is_route_whitelisted → false
 *   4.9  is_route_whitelisted: missing PDA → false (no revert)
 *   4.10 update_route_terms: tri-state Keep / Set / RevertToDefault
 *   4.11 add_admin: owner ✓; non-owner ✗
 *   4.12 remove_admin: flips is_active; previously-active admin loses authority
 *   4.13 add_admin re-activation: removed admin can be re-added (idempotent)
 *   4.14 whitelist_route re-activation: disabled route can be re-enabled
 *
 * All tests use Codama-generated typed Kit instruction builders from
 * `tests/clients/governance/` (regenerated by `pnpm gen-clients`). No
 * hand-rolled instruction discriminators.
 */

